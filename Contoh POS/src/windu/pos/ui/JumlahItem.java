    public JumlahItem(Penjualan p, PenjualanBarang pb) {
        initComponents();
        formPenjualan = p;
        penjualanBarang = pb;
        txtJumlah.requestFocusInWindow();
        formPenjualan.setEnabled(false);
        lblNamaBarang.setText(pb.getBarang().getNama());
        
        KeyStroke keyEnter = KeyStroke.getKeyStroke(KeyEvent.VK_F1, 0, false);
        KeyStroke keyEsc = KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0, false);
        
        panel.registerKeyboardAction(actionEnter, keyEnter, JComponent.WHEN_IN_FOCUSED_WINDOW);
        panel.registerKeyboardAction(actionEsc, keyEsc, JComponent.WHEN_IN_FOCUSED_WINDOW);
    }
    
    ActionListener actionEnter = new ActionListener() {
        public void actionPerformed(ActionEvent actionEvent) {
            enterEvent(actionEvent);
        }
    };
    
    ActionListener actionEsc = new ActionListener() {
        public void actionPerformed(ActionEvent actionEvent) {
            escEvent(actionEvent);
        }
    };
    
    private void escEvent(ActionEvent actionEvent) {
        formPenjualan.setEnabled(true);
        formPenjualan.setVisible(true);
        this.setVisible(false);
    }
    
    private void enterEvent(ActionEvent actionEvent) {
        try{
            int jumlah = Integer.parseInt(txtJumlah.getText());
            penjualanBarang.setJumlah(jumlah);
            formPenjualan.itemPenjualan.add(penjualanBarang);
            formPenjualan.createTable();
            
            formPenjualan.setEnabled(true);
            this.setVisible(false);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Hanya boleh angka", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

