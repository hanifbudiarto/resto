package windu.pos.entity;

/**
 *
 * @author WinduPurnomo
 */
public class Pelanggan {
    private int id;
    private String nama;
    private String noHP;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNoHP() {
        return noHP;
    }

    public void setNoHP(String noHP) {
        this.noHP = noHP;
    }
}
